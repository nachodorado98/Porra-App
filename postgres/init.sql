CREATE TABLE grupo_equipos_real (Grupo CHAR(1),
									Equipo_Id VARCHAR(255),
									Posicion INTEGER,
									PRIMARY KEY (Grupo, Equipo_Id),
									FOREIGN KEY (Grupo) REFERENCES grupos (Grupo) ON DELETE CASCADE,
									FOREIGN KEY (Equipo_Id) REFERENCES equipos (Equipo_Id) ON DELETE CASCADE,
									CHECK (Posicion BETWEEN 1 AND 4));

CREATE TABLE grupo_equipos_porra (Usuario VARCHAR(255),
									Grupo CHAR(1),
									Equipo_Id VARCHAR(255),
									Posicion INTEGER,
									Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
									PRIMARY KEY (Usuario, Grupo, Equipo_Id),
									FOREIGN KEY (Usuario) REFERENCES usuarios (Usuario) ON DELETE CASCADE,
									FOREIGN KEY (Grupo) REFERENCES grupos (Grupo) ON DELETE CASCADE,
									FOREIGN KEY (Equipo_Id) REFERENCES equipos (Equipo_Id) ON DELETE CASCADE,
									CHECK (Posicion BETWEEN 1 AND 4));

CREATE TABLE mejores_terceros_porra (Usuario VARCHAR(255),
										Grupo CHAR(1),
										Equipo_Id VARCHAR(255),
										Orden INTEGER,
										Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
										PRIMARY KEY (Usuario, Grupo, Equipo_Id),
										FOREIGN KEY (Usuario) REFERENCES usuarios (Usuario) ON DELETE CASCADE,
										FOREIGN KEY (Grupo) REFERENCES grupos (Grupo) ON DELETE CASCADE,
										FOREIGN KEY (Equipo_Id) REFERENCES equipos (Equipo_Id) ON DELETE CASCADE,
										CHECK (Orden BETWEEN 1 AND 8));